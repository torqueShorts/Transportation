package Transport.Model;

public class TrafficOfficer {

    private String trafficID;
    private String name;
    private String surname;
    private String fineDue;

    public TrafficOfficer(String trafID, String nm, String surnm, String finDue)
    {
        trafficID = trafID;
        name = nm;
        surname = surnm;
        fineDue = finDue;
    }



}
